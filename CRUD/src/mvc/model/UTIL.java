package mvc.model;

public class UTIL {
	
	private String user;
	private String day;
	private String workout;
	private String replace;
	
	public String getUser() { return this.user; }
	
	public String getDay() { return this.day; }
	
	public String getWorkout() { return this.workout; }
	
	public String getReplace() { return this.replace; }
	
	public void setReplace(String replace) { this.replace = replace; }	
	
	public void setWorkout(String workout) { this.workout = workout; }	
	
	public void setDay(String day) { this.day = day; }
		
	public void setUser(String user) { this.user = user; }

}
